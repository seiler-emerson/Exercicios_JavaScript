/*
    Escreva um programa que mostre a tabuada de vários números, um de cada vez,
    para cada valor digitado pelo usuário.

*/
let numero = 4

console.log("============")
for (let i = 1; i <= 10 ; i++) {
    res = numero * i
    console.log(`${numero} x ${i} = ${res}`)
}

console.log("============")
