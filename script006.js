/*
    Escreva um programa que leia um número e mostre o seu dobro, triplo e raiz quadrada.
*/

let entrada = 85

let dobro = entrada * 2
let triplo = entrada * 3
let raiz = Math.sqrt(entrada)

console.log(`O dobro de ${entrada} vale ${dobro}.`)
console.log(`O triplo de ${entrada} vale ${triplo}.`)
console.log(`A raiz quadrada de ${entrada} é igual a ${raiz}.`)